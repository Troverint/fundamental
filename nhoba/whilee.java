package nhoba;

public class whilee{
    public static void main(String[] args) {



        int count = 1;
        while (count <= 10) {
            System.out.printf("%d ", count);
            ++count;
        }


        for(int i = 10; i > 20; i++){
            System.out.printf("%d ", i);
        }

        
        do{
            System.out.printf("%d ", count);
            ++count;
        }while(count <= 10);








    }
}
